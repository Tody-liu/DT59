public class TypeDefaultValue {
    // 数据类型：byte 默认值为：0
    byte b;

    // 数据类型：short 默认值为：0
    short s;

    // 数据类型：int 默认值为：0
    int i;

    // 数据类型：long 默认值为：0
    long l;

    // 数据类型：float 默认值为：0.0
    float f;

    // 数据类型：double 默认值为：0.0
    double d;

    // 数据类型：char 默认值为： (空格)
    char c;

    // 数据类型：boolean 默认值为：false
    boolean b1;

    // 数据类型：String 默认值为：null
    String name;

    // 数据类型：int[] 默认值为：null
    int[] array;
}
